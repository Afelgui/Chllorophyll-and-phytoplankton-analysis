function [mask,x,y,z,dx,dy,h]=getgrid()
mh=310;
nh=420;
kh=40;
ff=fopen('grid2.dat','rb');
n=fread(ff,1,'int');
x=fread(ff,[mh,nh],'double');
y=fread(ff,[mh,nh],'double');
z=fread(ff,kh,'double');
dx=fread(ff,[mh,nh],'double');
dy=fread(ff,[mh,nh],'double');
fclose(ff);
ff=fopen('bound2.dat','rb');
n=fread(ff,1,'int');
kp=fread(ff,[mh,nh],'integer*4');
kp0=fread(ff,[mh,nh],'integer*2');
kp0=fread(ff,[mh,nh],'integer*2');
mask=fread(ff,[mh,nh],'integer*2');
fclose(ff);
ff=fopen('top2.dat','rb');
n=fread(ff,1,'int');
h=fread(ff,[mh,nh],'double');
fclose(ff);
idx=find(mask==0);
h(idx)=NaN;
x=x(2:end-1,2:end-1);
y=y(2:end-1,2:end-1);
dx=dx(2:end-1,2:end-1);
dy=dy(2:end-1,2:end-1);
z=z(2:end-1);
h=h(2:end-1,2:end-1);
mask=mask(2:end-1,2:end-1);
kp=kp(2:end-1,2:end-1);
